#Program converting Kg into pounds

number = 0
times_table = 2.2
result = number * times_table
while number < 21:
    print("{0:>2} Kg X {1:>2} = {2:>2.2f} pounds".format(number, times_table, result))
    number = number + 1
    result = number * times_table
           
    
    
    
        
  
